var status = -1;
function action(mode, type, selection) {
	if (mode == 1) {
		status++;
	} else {
		status--;
	}

	switch (status) {
		case 0:
			{
				var text = "デバッグショップ\r\n";
				text += "#L" + 1 + "##r書#k#l\r\n";
				text += "#L" + 2 + "##r手裏剣と弾丸#k#l\r\n";
				return cm.sendSimple(text);
			}
		case 1:
			{

				switch (selection) {
					case 1:
						{
							/*
							var shop = cm.CreateCustomShop(cm.getNpc());
							for (var i = 0; i < item_scrolls.length; i++) {
								cm.CustomShopAdd(shop, item_scrolls[i], 1000);
							}
							cm.OpenCustomShop(shop);
							*/
							CustomShop(item_scrolls);
							break;
						}
					case 2:
						{
							CustomShop(item_throw);
							break;
						}
					default:
						break;
				}
				break;
			}
		default:
			break;
	}
	cm.dispose();
}

function CustomShop(itemlist) {
	var shop = cm.CreateCustomShop(cm.getNpc());
	for (var i = 0; i < itemlist.length; i++) {
		cm.CustomShopAdd(shop, itemlist[i], 1000);
	}
	cm.OpenCustomShop(shop);
}


